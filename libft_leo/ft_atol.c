#include "libft.h"

long	ft_atol(const char *s)
{
	int		i;
	long	ret;
	int		sign;

	i = 0;
	ret = 0;
	sign = 1;
	while ((s[i] >= 9 && s[i] <= 13) || s[i] == 32)
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] >= 48 && s[i] <= 57)
	{
		ret = ret * 10 + (s[i] - 48);
		i++;
	}
	return (ret * sign);
}
